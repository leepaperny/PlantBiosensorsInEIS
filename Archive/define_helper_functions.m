Z_R = (@(R, omega) R + 0 * omega);
Z_C = (@(C, omega) 1 ./ (j .* omega .* C));
Z_leaky_C = (@(C, exp_factor, omega) 1 ./ ((j .* omega) .^ exp_factor .* C));
Z_W = (@(A, omega) A ./ sqrt(j .* omega));

pll2 = (@(x, y) 1 ./ ((1 ./ x) + (1 ./ y)));

randles_model_with_leaky_cap_impedance = (@(params, omega) ...
	pll2( ...
		( ...
			Z_R(params(1), omega) ...
			+ Z_W(params(4), omega) ...
		), ...
		Z_leaky_C(params(2) * 1e-6, params(5), omega) ...
	) ...
	+ Z_R(params(3), omega) ...
);
