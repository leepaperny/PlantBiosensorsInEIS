STATISTICS_FILENAME = "fitting_stats.csv";

statistics_table = table();
row_labels = {'filename', 'R_ct (ohm)', 'C_dl (uF)', 'R_s (ohm)', 'A_W (ohm s^0.5)', 'leaky exponent', 'fitting MRE (%)'};

for iter = 1:length(measurements)
	current_row = table( ...
		measurements{iter}.filename, ...
		measurements{iter}.fit_params(1), ...
		measurements{iter}.fit_params(2), ...
		measurements{iter}.fit_params(3), ...
		measurements{iter}.fit_params(4), ...
		measurements{iter}.fit_params(5), ...
		measurements{iter}.fitting_mre * 100 ...
	);

	statistics_table = [statistics_table; current_row];
end

statistics_table.Properties.VariableNames = row_labels;

writetable(statistics_table, STATISTICS_FILENAME);
